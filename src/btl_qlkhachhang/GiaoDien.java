    //Sửa dơn hàng
    private void btnSuaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDonActionPerformed
        // TODO add your handling code here:
        boolean boo = false;
        int d = -1;
        for (DonHang k : dsDH) {
            if (k.getMadh().equals(txtMaThuoc.getText())) {
                DonHang dh = new DonHang();
                dh.setMadh(txtMaThuoc.getText());
                dh.setMakh(txtMaKhach.getText());
                dh.setTenThuoc(txtTenThuoc.getText());
                dh.setTien(Float.parseFloat(txtTienThuoc.getText()));
                dsDH.set(d + 1, dh);
                try {
                    // Lưu danh sách đơn hàng vào file txt
                    FileUtils.ghiObjectVaoFile("donhang.txt", dsDH);
                } catch (IOException e) {
                    System.out.println("khong luu dc file");
                }
                loadDHTable(dsDH);
                clearDH();
                boo = true;
                break;
            } else {
                d++;
            }
        }
        if (!boo) {
            JOptionPane.showMessageDialog(rootPane, "Mã đơn không tồn tại, không thể sửa");
        }
    }//GEN-LAST:event_btnSuaDonActionPerformed

