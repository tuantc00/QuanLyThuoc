    //Thêm khách hàng
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        boolean boo = true;
        String mess = "";
        if ("".equals(txtMaKH.getText())) {
            mess = "Mã khách hàng không được để trống";
        } else if ("".equals(txtTenKH.getText())) {
            mess = "Tên khách hàng không được để trống";
        } else if ("".equals(txtDiaChi.getText())) {
            mess = "Địa chỉ không được để trống";
        } else if ("".equals(txtSDT.getText())) {
            mess = "Số điện thoại không được để trống";
        } else if ("".equals(txtTuoi.getText())) {
            mess = "Số tuổi không được để trống";
        } else if (rdNam.isSelected() == false && rdNu.isSelected() == false) {
            mess = "Chưa chọn giới tính";
        }

        if (!mess.equals("")) {
            JOptionPane.showMessageDialog(rootPane, mess);
        } else {
            for (KhachHang k : dsKH) {
                if (k.getMakh().equals(txtMaKH.getText())) {
                    boo = false;
                    break;
                }
            }
            if (boo) {
                KhachHang kh = new KhachHang();
                kh.setMakh(txtMaKH.getText());
                kh.setTenKh(txtTenKH.getText());
                kh.setDiaChi(txtDiaChi.getText());
                kh.setSoDt(txtSDT.getText());
                kh.setTuoi(Integer.parseInt(txtTuoi.getText()));
                kh.setMakh(txtMaKH.getText());
                if (rdNam.isSelected()) {
                    kh.setGioiTinh("Nam");
                }
                if (rdNu.isSelected()) {
                    kh.setGioiTinh("Nu");
                }
                dsKH.add(kh);
                try {
                    // Lưu danh sách đơn hàng vào file txt
                    FileUtils.ghiObjectVaoFile("khachhang.txt", dsKH);
                } catch (IOException e) {
                    System.out.println("khong luu dc file");
                }
                loadKHTable(dsKH);
                clearKH();
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Mã khách hàng đã tồn tại");
            }
        }


    }//GEN-LAST:event_btnThemActionPerformed

