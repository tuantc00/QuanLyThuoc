    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int dong = tbKhachHang.getSelectedRow();
        if (dong != -1) {
            KhachHang kh = dsKH.get(dong);

            // Kiểm tra xem khách hàng có đơn hàng nào không
            boolean coDonHang = false;
            for (DonHang dh : dsDH) {
                if (dh.getMakh().equals(kh.getMakh())) {
                    coDonHang = true;
                    break;
                }
            }

            if (coDonHang) {
                JOptionPane.showMessageDialog(rootPane, "Không thể xóa khách hàng đã có đơn hàng!");
            } else {
                int luaChon = JOptionPane.showConfirmDialog(
                        rootPane,
                        "Bạn có chắc chắn muốn xóa khách hàng này?",
                        "Xác nhận xóa",
                        JOptionPane.YES_NO_OPTION
                );

                if (luaChon == JOptionPane.YES_OPTION) {
                    dsKH.remove(dong);

                    try {
                        FileUtils.ghiObjectVaoFile("khachhang.txt", dsKH);
                        loadKHTable(dsKH);
                        clearKH();
                        JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    } catch (IOException e) {
                        JOptionPane.showMessageDialog(rootPane, "Xảy ra lỗi khi xóa dữ liệu");
                    }
                } 
            } 
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn khách hàng cần xóa!");
        }

    }//GEN-LAST:event_btnXoaActionPerformed

